package My_classes;

import java.sql.Date;

public class Library {

    private int id;
    private String book_name;
    private String author;
    private int year;
    private String genre;

    public Library() {
    }

    public int getId() {
        return id;
    }

    public String getBookName() {
        return book_name;
    }

    public String getAuthor() {
        return author;

    }

    public int getYear() {
        return year;
    }

    public String getGenre() {
        return genre;
    }

}
